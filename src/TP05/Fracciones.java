package TP05;

public class Fracciones {
    public static void main(String[] args) {

        float numerador1 = 6;                           // CONSTRUYO LAS FRACCIONES
        float denominador1 = 5;
        float fraccion1 = numerador1 / denominador1;

        float numerador2 = 7;
        float denominador2 = 3;
        float fraccion2 = numerador2 / denominador2;

        float numerador3 = 1;
        float denominador3 = 15;
        float fraccion3 = numerador3 / denominador3;

        float numerador4 = 1;
        float denominador4 = 15;
        float fraccion4 = numerador4 / denominador4;

        float resultadoFinal = (fraccion1 - fraccion2 + fraccion3) / fraccion4;

        System.out.println(resultadoFinal);














    }
}
